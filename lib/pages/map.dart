class Location {
  final String name;
  final LatLng locate;
  final String url;

  const Location({
    this.name,
    this.locate,
    this.url,
  });
}

class CustomSearchDelegate extends SearchDelegate {
  final List<Location> _place;

  CustomSearchDelegate(this._place)
      : super(
          searchFieldLabel: "ค้นหาภายในมหาวิทยาลัยพะเยา",
          keyboardType: TextInputType.text,
          textInputAction: TextInputAction.search,
        );

  @override
  ThemeData appBarTheme(BuildContext context) {
    assert(context != null);
    final ThemeData theme = Theme.of(context);
    assert(theme != null);
    return theme.copyWith(primaryColor: Colors.purple);
  }

  @override
  List<Widget> buildActions(BuildContext context) {
    return [
      IconButton(
        icon: Icon(Icons.clear),
        onPressed: () {
          query = '';
        },
      ),
    ];
  }

  @override
  Widget buildLeading(BuildContext context) {
    return IconButton(
      icon: Icon(Icons.arrow_back),
      onPressed: () {
        close(context, null);
      },
    );
  }

  @override
  Widget buildResults(BuildContext context) {
    if (query.isNotEmpty) {
      return Column(
        children: _place
            .where((p) => p.name.trim().startsWith(query.trim()))
            .map<Widget>((p) => ListTile(
                  title: Text(p.name),
                  subtitle: Text("${p.locate.latitude}, ${p.locate.longitude}"),
                  onTap: () {
                    context.read<AppState>().sendRequest(
                          name: p.name,
                          address: p.locate,
                          url: p.url,
                        );
                    close(context, null);
                  },
                ))
            .toList(),
      );
    }

    return Column();
  }

  @override
  Widget buildSuggestions(BuildContext context) {
    return Column();
  }
}
